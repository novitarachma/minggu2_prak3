        manajerial, desain, dan pembuatan sistem jaringan dan perangkat pendukungnya, yang berkarakter technopreneur, 
        sadar mutu dengan keunggulan kompetitif yang beretika profesi.</br>

        <br>Program Studi Manajemen Informatika, Konsentrasi Multimedia dirancang secara khusus guna menghasilkan 
        tenaga ahli madya bidang multimedia, yang memiliki kompetensi bidang manajerial, desain, dan pembuatan perangkat 
        lunak berupa pengolahan teks, audio, video, image, animasi perangkat mobile, desktop, dan jaringan, yang 
        berkarakter technopreneur, sadar mutu dengan keunggulan kompetitif yang beretika profesi.</br>
    
