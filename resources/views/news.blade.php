    <h2>Keputusan Hari H Pemilu 2024 Diundur Paling Lambat Awal Oktober 2021</h2>
    <h3><br>Liputan6.com, Jakarta - Ketua Komisi II DPR Ahmad Doli Kurnia menyatakan, ada dua versi usulan hari-H Pemilu 
            2024. Pertama KPU usul pada Rabu 21 Februari 2024, sementara Mendagri mengusulkan hari-H pemilu dilaksanakan 
            pada April atau bahkan Mei 2024.</br>

        <br>Oleh karena itu, pihaknya memutuskan memundurkan jadwal pengambilan keputusan hari H Pemilu 2024, untuk 
        mengumpulkan masukan dan melakukan konsinyering.</br>

        <br>“Saran saya kalau kita bahas dua-duanya sekarang ini dua hari dua malam mungkin enggak selesai, karena itu 
        saya mengusulkan sisa waktu ini bapak-ibu beri masukan, dan kemudian kita sepakati hari ini belum mungkin ambil 
        keputusan,” kata Doli dalam rapat Komisi II DPR, Kamis (16/9/2021).</br>

        <br>Doli menyatakan pihaknya akan menentukan hari-H Pemilu 2024 sebelum memasuki masa reses, atau selambat-
        lambatnya awal Oktober 2021.</br>

        <br>“Kita membutuhkan waktu, kita sepakati sebelum berakhir masa sidang kita harus ambil keputusan, paling lama 
        awal Oktober sebelum masa reses,” katanya.</br>

        <br>Keputusan Tidak Hari Ini</br>

        <br>Sebelumnya, Mendgari Tito meminta penentuan hari-H pemilu tidak diambil pada rapat Komisi II hari ini.</br>

        <br>“Kami meminta penentuan waktu pemungutan suara 2024 diputuskan di rapat kerja Komisi II, di rapat berikutnya, 
            sebelum masa reses. Dalam kurun waktu ini pemeintah akan rapat inernal,” kata Mendagri.</br></h3>
